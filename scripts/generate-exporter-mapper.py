def generate_stub_mapper(fmt: str, headers: list[str]) -> str:
    col_comment = ", ".join(headers)
    fn_name = "export" + fmt.capitalize()
    return textwrap.dedent(f"""\
        /**
         * {fmt.upper()} exporter stub — generated by scripts/generate-exporter-mapper.py
         * Columns: {col_comment}
         * TODO: implement mapping logic for this format.
         */
        function {fn_name}(rows, criteria) {{
          console.warn('{fn_name}: not yet implemented');
        }}
        """)


def read_settings(folder: Path) -> dict:
    settings_path = folder / "settings.json"
    if settings_path.exists():
        with open(settings_path, encoding="utf-8") as f:
            return json.load(f)
    return {}


def generate_registry(exporters: list[dict]) -> str:
    lines = ["/* Generated by scripts/generate-exporter-mapper.py — do not edit by hand. */",
             "window.RADIOMAP_EXPORTERS = ["]
    for i, meta in enumerate(exporters):
        parts = [
            f"id: '{meta['id']}'",
            f"label: '{meta['label']}'",
            f"fn: '{meta['fn']}'",
        ]
        if meta.get("url"):
            parts.append(f"url: '{meta['url']}'")
        comma = "," if i < len(exporters) - 1 else ""
        lines.append("  { " + ", ".join(parts) + " }" + comma)
    lines.append("];\n")
    return "\n".join(lines)


def main() -> None:
    if not EXPORTER_ROOT.is_dir():
        raise SystemExit(f"Exporter root not found: {EXPORTER_ROOT}")

    exporters: list[dict] = []

    for ref in sorted(EXPORTER_ROOT.glob("*/reference.csv")):
        fmt = ref.parent.name
        headers = read_headers(ref)
        if not headers:
            print(f"[SKIP] {fmt}: reference.csv has no headers")
            continue

        if fmt == "chirp":
            content = generate_chirp_mapper(headers)
        elif fmt == "ft5dr":
            content = generate_ft5dr_mapper(headers)
        elif fmt == "ftm150":
            content = generate_ftm150_mapper(len(headers))
        else:
            content = generate_stub_mapper(fmt, headers)

        out_path = ref.parent / "mapper.js"
        out_path.write_text(content, encoding="utf-8")
        print(f"[OK]   {fmt}: wrote {out_path} ({len(headers)} columns)")

        settings = read_settings(ref.parent)
        exporters.append({
            "id": fmt,
            "label": settings.get("label", fmt.upper()),
            "fn": settings.get("fn", "export" + fmt.capitalize()),
            "url": settings.get("url", ""),
        })

    registry_path = EXPORTER_ROOT / "registry.js"
    registry_path.write_text(generate_registry(exporters), encoding="utf-8")
    print(f"[OK]   registry: wrote {registry_path} ({len(exporters)} exporter(s))")


if __name__ == "__main__":
    main()
